package com.example.marketmapping;

public class ShowListNamesItem {
    private String mListNamesShowList;
    private String mStoreNameShowList;

    public ShowListNamesItem(String ListNamesShowList, String StoreNameShowList) {
        mListNamesShowList = ListNamesShowList;
        mStoreNameShowList = StoreNameShowList;

    }

    public String getListNamesShowList() {
        return mListNamesShowList;
    }

    public String getStoreNameShowList() {
        return mStoreNameShowList;
    }

}
